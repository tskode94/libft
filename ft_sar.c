#include "libft.h"

char	*ft_sar(char *s1, char *s2, char *s3)
{
	int	i;

	i = 0;
	while (s1[i] != '\0')
	{
		if (s1[i] == s2[0])
			s1[i] = s3[0];
		i++;
	}
	return (s1);
}
